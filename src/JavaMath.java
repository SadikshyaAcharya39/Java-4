public class JavaMath {
    public static void main(String[] args) {

        System.out.println(Math.random()*101);
        System.out.println(Math.max(20, 1));
        System.out.println(Math.min(30.04, 30.02));
        System.out.println(Math.ceil(20.90));
        System.out.println(Math.floor(20.09));
        System.out.println(Math.round(20.03));
        System.out.println(Math.sqrt(100));
    }
}
